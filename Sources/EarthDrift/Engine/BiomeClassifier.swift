import Foundation
import CoreLocation

struct BiomeClassifier {
    func classify(coordinate: CLLocationCoordinate2D, altitude: Double, routeCategory: RouteCategory?) -> BiomeMix {
        var weights: [Biome: Float] = [:]

        let lat = coordinate.latitude

        if abs(lat) > 66 {
            return BiomeMix(single: .arctic)
        }

        if abs(lat) > 60 {
            weights[.arctic] = 0.3
        }

        let dominant = dominantBiome(for: coordinate, routeCategory: routeCategory)
        weights[dominant, default: 0] += 0.7

        if altitude > 8000 {
            let w = weights[.mountain, default: 0]
            weights.removeAll()
            weights[.mountain] = w + 1.0
            return BiomeMix(weights: weights)
        }

        if let category = routeCategory {
            if category == .islandHopping || category == .coastlines {
                weights[.coastal, default: 0] += 0.3
            }
        }

        return BiomeMix(weights: weights)
    }

    private func dominantBiome(for coordinate: CLLocationCoordinate2D, routeCategory: RouteCategory?) -> Biome {
        let isOverOcean = isCoordinateOverOcean(coordinate)

        if isOverOcean {
            if let category = routeCategory {
                switch category {
                case .islandHopping: return .coastal
                case .coastlines: return .coastal
                default: return .ocean
                }
            }
            return .ocean
        }

        if let category = routeCategory {
            return category.dominantBiome
        }

        return .grassland
    }

    private func isCoordinateOverOcean(_ coordinate: CLLocationCoordinate2D) -> Bool {
        let lat = coordinate.latitude
        let lon = coordinate.longitude

        if lon > 180 || lon < -180 { return true }
        if lat > 90 || lat < -90 { return true }

        let normalizedLon = ((lon + 180).truncatingRemainder(dividingBy: 360) + 360).truncatingRemainder(dividingBy: 360) - 180

        let lonIdx = Int((normalizedLon + 180) / 2.0) % 180
        let latIdx = Int((lat + 90) / 2.0)

        guard latIdx >= 0, latIdx < 90, lonIdx >= 0, lonIdx < 180 else { return false }

        let byteIndex = latIdx * 23 + lonIdx / 8
        let bitIndex = lonIdx % 8

        guard byteIndex >= 0, byteIndex < oceanMask.count else { return false }

        let byte = oceanMask[byteIndex]
        return (byte & (1 << (7 - bitIndex))) != 0
    }
}

extension RouteCategory {
    var dominantBiome: Biome {
        switch self {
        case .rivers: return .river
        case .railways: return .grassland
        case .flights: return .urban
        case .trails: return .forest
        case .coastlines: return .coastal
        case .ancient: return .desert
        case .islandHopping: return .coastal
        case .mountains: return .mountain
        case .volcanoes: return .mountain
        case .deserts: return .desert
        case .polar: return .arctic
        case .cities: return .urban
        }
    }

    var channelSoundFile: String? {
        switch self {
        case .rivers: return "river"
        case .railways: return "railway"
        case .flights: return "flight"
        case .coastlines, .islandHopping: return "coastal_surf"
        case .ancient, .deserts: return "caravan"
        case .trails, .mountains, .volcanoes, .polar, .cities: return nil
        }
    }
}

